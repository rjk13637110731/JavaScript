<script>
        console.log('小猪佩奇' + 18);  //'小猪佩奇18'
        console.log('小猪佩奇' + 18 + '岁');  //'小猪佩奇18岁'

        var age = 18;
        //console.log('小猪佩奇age岁');   //不行
        // 我们变量不要写到字符串里面，是通过和字符串相连的方式实现的
        console.log('小猪佩奇' + age + '岁');  //'小猪佩奇18岁'

        // 变量和字符串相连的口诀：引引加加
        console.log('小猪佩奇' + age + '岁');
</script>
